import math

x = 3.7
y = 4

print(f'Pierwiastek kwadratowy z x: {math.sqrt(x)}')
print(f'X do potęgi Y: {math.pow(x, y)}')
print(f'Pierwiastek stopnia y z x: {math.pow(x, 1 / y)}')
print(f'Pole koła o promieniu x: {math.pi * math.pow(x, 2)}')
print(f'Silnia y: {math.factorial(y)}')
print(f'Największa możliwa liczba całkowita mniejsz bądz równa x: {math.floor(x)}')
